package BASICS;

public class relationalOperators {
    public static void main(String[] args) {
        boolean x= 8>9;
        System.out.println(x);
        x= 8<9;

        System.out.println(x);
        x= 8==9;

        System.out.println(x);
        x= 8!=9;

        System.out.println(x);
        x= 8>=9;
        System.out.println(x);
        x = 8<=9;
        System.out.println(x);
    }
}
